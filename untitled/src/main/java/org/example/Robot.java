package org.example;

public class Robot {
    public void name(String name){
        System.out.println("The robot is called " + name);
    }
    public void sleep() {
        System.out.println("The robot is sleeping");
    }
    public void eat() {
        System.out.println("The robot is eating");
    }
    public void jump() {
        System.out.println("The robot is jumping");
    }
}
